    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (getProductMeasureUnit() == null) {
            setProductMeasureUnit(new ProductMeasureUnit());
            getProductMeasureUnit().setDateRegistration(Calendar.getInstance().getTime());
        }
        getProductMeasureUnit().setActive(getjCheckBox1().isSelected());
        getProductMeasureUnit().setNameMeasure(getjTextField1().getText());
        getProductMeasureUnit().setShortNameMeasure(getjTextField2().getText());
        getProductMeasureUnitController().save(getProductMeasureUnit());
        JOptionPane.showMessageDialog(getjDialog(), "Sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        setMeasureUnitNames();
        setTable();
        cleanFields();
        setProductMeasureUnit(null);
        getjButton4().setEnabled(false);
        resetJCheckBox();


    }//GEN-LAST:event_jButton1ActionPerformed

