    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir a unidade de medida de produto selecionada?", "Tem certeza?", JOptionPane.YES_NO_CANCEL_OPTION);
        if (resposta == 0) {
            String unidadeMedida = (String) getjList1().getModel().getElementAt(getjList1().getSelectedIndex());
            ProductMeasureUnit productMeasureUnit = new ProductMeasureUnit();
            String[] unidadeList = getProductMeasureUnitController().splitNameInList(unidadeMedida);
            productMeasureUnit = getProductMeasureUnitController().findByNameMeasureNShortNameMeasure(unidadeList[0], unidadeList[2]);
            getProductMeasureUnitController().remove(productMeasureUnit);
            cleanFields();
            setMeasureUnitNames();
            setTable();
            getjButton4().setEnabled(false);
            resetJCheckBox();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente editar a unidade de medida de produto selecionadaa?", "Tem certeza?", JOptionPane.YES_NO_CANCEL_OPTION);
            if (resposta == 0) {
                getjButton4().setEnabled(true);
                getjCheckBox1().setEnabled(true);
                String unidadeMedida = (String) getjList1().getModel().getElementAt(getjList1().getSelectedIndex());
                ProductMeasureUnit productMeasureUnit = new ProductMeasureUnit();
                String[] unidadeList = getProductMeasureUnitController().splitNameInList(unidadeMedida);
                productMeasureUnit = getProductMeasureUnitController().findByNameMeasureNShortNameMeasure(unidadeList[0], unidadeList[2]);
                getjTextField1().setText(productMeasureUnit.getNameMeasure());
                getjTextField2().setText(productMeasureUnit.getShortNameMeasure());
                getjCheckBox1().setSelected(productMeasureUnit.getActive());
                setProductMeasureUnit(productMeasureUnit);
            }
        }
    }//GEN-LAST:event_jList1MouseClicked

