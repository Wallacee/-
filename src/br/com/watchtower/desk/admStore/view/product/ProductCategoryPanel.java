    private void setCategoryNames() {
        List<ProductCategory> productCategorys = getProductCategoryController().findAll();
        getjList1().setModel(new DataList(getStringsVector(productCategorys)));

    }

    private String[] getStringsVector(List<ProductCategory> productCategorys) {
        String[] strings = new String[productCategorys.size()];
        int count = 0;
        for (ProductCategory pCategory : productCategorys) {
            strings[count] = pCategory.getCategory();
            ++count;
        }
        return strings;

    }

