    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente editar a categoria de produto selecionada?", "Tem certeza?", JOptionPane.YES_NO_CANCEL_OPTION);
            if (resposta == 0) {
                getjButton1().setEnabled(true);
                getjCheckBox1().setEnabled(true);
                String categoria = (String) getjList1().getModel().getElementAt(getjList1().getSelectedIndex());
                ProductCategory productCategory = new ProductCategory();
                productCategory = getProductCategoryController().findByCategory(categoria);
                getjTextField1().setText(productCategory.getCategory());
                getjCheckBox1().setSelected(productCategory.getActive());
                setProductCategory(productCategory);
            }
        }
    }//GEN-LAST:event_jList1MouseClicked

