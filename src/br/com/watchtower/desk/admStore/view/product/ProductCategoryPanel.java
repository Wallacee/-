    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (getProductCategory() == null) {
            setProductCategory(new ProductCategory());
            getProductCategory().setDateRegistration(Calendar.getInstance().getTime());
        }
        getProductCategory().setActive(getjCheckBox1().isSelected());
        getProductCategory().setCategory(getjTextField1().getText());
        getProductCategoryController().save(getProductCategory());
        JOptionPane.showMessageDialog(getjDialog(), "Sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        setCategoryNames();
        setTable();
        cleanFields();
        setProductCategory(null);
        getjButton1().setEnabled(false);
        resetCheckBox();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir a categoria de produto selecionada?", "Tem certeza?", JOptionPane.YES_NO_CANCEL_OPTION);
        if (resposta == 0) {
            String categoria = (String) getjList1().getModel().getElementAt(getjList1().getSelectedIndex());
            ProductCategory productCategory = new ProductCategory();
            productCategory = getProductCategoryController().findByCategory(categoria);
            getProductCategoryController().remove(productCategory);
            cleanFields();
            setCategoryNames();
            setTable();
            getjButton1().setEnabled(false);
            resetCheckBox();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

