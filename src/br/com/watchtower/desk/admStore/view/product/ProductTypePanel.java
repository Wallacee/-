    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (getProductType() == null) {
            setProductType(new ProductType());
            getProductType().setDateRegistration(Calendar.getInstance().getTime());
        }
        getProductType().setActive(getjCheckBox1().isSelected());
        getProductType().setType(getjTextField1().getText());
        getProductTypeController().save(getProductType());
        JOptionPane.showMessageDialog(getjDialog(), "Sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        setTypeNames();
        setTable();
        cleanFields();
        setProductType(null);
        getjButton2().setEnabled(false);
        resetJCheckBox();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o tipo de produto selecionado?", "Tem certeza?", JOptionPane.YES_NO_CANCEL_OPTION);
        if (resposta == 0) {
            String tipo = (String) getjList1().getModel().getElementAt(getjList1().getSelectedIndex());
            ProductType productType = new ProductType();
            productType = getProductTypeController().findByType(tipo);
            getProductTypeController().remove(productType);
            cleanFields();
            setTypeNames();
            setTable();
            getjButton2().setEnabled(false);
            resetJCheckBox();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente editar o tipo de produto selecionado?", "Tem certeza?", JOptionPane.YES_NO_CANCEL_OPTION);
            if (resposta == 0) {
                getjCheckBox1().setEnabled(true);
                getjButton2().setEnabled(true);
                String tipo = (String) getjList1().getModel().getElementAt(getjList1().getSelectedIndex());
                ProductType productType = new ProductType();
                productType = getProductTypeController().findByType(tipo);
                getjTextField1().setText(productType.getType());
                getjCheckBox1().setSelected(productType.getActive());
                setProductType(productType);
            }
        }
    }//GEN-LAST:event_jList1MouseClicked

