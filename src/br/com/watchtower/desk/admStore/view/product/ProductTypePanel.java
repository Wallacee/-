    private void setTypeNames() {
        List<ProductType> productTypes = getProductTypeController().findAll();
        getjList1().setModel(new DataList(getStringsVector(productTypes)));

    }

    private String[] getStringsVector(List<ProductType> productTypes) {
        String[] strings = new String[productTypes.size()];
        int count = 0;
        for (ProductType pType : productTypes) {
            strings[count] = pType.getType();
            ++count;
        }
        return strings;

    }

