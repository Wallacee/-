    private void setDetailNames() {
        List<ProductDetail> productDetails = getProductDetailController().findAll();
        getjList1().setModel(new DataList(getStringsVector(productDetails)));
    }

    private String[] getStringsVector(List<ProductDetail> productDetails) {
        String[] strings = new String[productDetails.size()];
        int count = 0;
        for (ProductDetail pDetail : productDetails) {
            strings[count] = pDetail.getDetail();
            ++count;
        }
        return strings;
    }

