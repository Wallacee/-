    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir a especificação de produto selecionada?", "Tem certeza?", JOptionPane.YES_NO_CANCEL_OPTION);
        if (resposta == 0) {
            String detalhe = (String) getjList1().getModel().getElementAt(getjList1().getSelectedIndex());
            ProductDetail productDetail = new ProductDetail();
            productDetail = getProductDetailController().findByDetailName(detalhe);
            getProductDetailController().remove(productDetail);
            cleanFields();
            setDetailNames();
            setTable();
            getjButton1().setEnabled(false);
            resetCheckBox();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente editar a especificação de produto selecionada?", "Tem certeza?", JOptionPane.YES_NO_CANCEL_OPTION);
            if (resposta == 0) {
                getjButton1().setEnabled(true);
                getjCheckBox1().setEnabled(true);
                String detalhe = (String) getjList1().getModel().getElementAt(getjList1().getSelectedIndex());
                ProductDetail productDetail = new ProductDetail();
                productDetail = getProductDetailController().findByDetailName(detalhe);
                getjTextField1().setText(productDetail.getDetail());
                getjCheckBox1().setSelected(true);
                setProductDetail(productDetail);
            }
        }
    }//GEN-LAST:event_jList1MouseClicked

