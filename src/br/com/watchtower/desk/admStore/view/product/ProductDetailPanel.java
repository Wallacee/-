    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (getProductDetail() == null) {
            setProductDetail(new ProductDetail());
            getProductDetail().setDateRegistration(Calendar.getInstance().getTime());
        }
        getProductDetail().setActive(getjCheckBox1().isSelected());
        getProductDetail().setDetail(getjTextField1().getText());
        getProductDetailController().save(getProductDetail());
        JOptionPane.showMessageDialog(getjDialog(), "Sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        setDetailNames();
        setTable();
        cleanFields();
        setProductDetail(null);
        getjButton1().setEnabled(false);
        resetCheckBox();
    }//GEN-LAST:event_jButton2ActionPerformed

